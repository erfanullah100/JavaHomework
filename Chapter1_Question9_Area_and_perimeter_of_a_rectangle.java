package chapter1_question9_area_and_perimeter_of_a_rectangle;


public class Chapter1_Question9_Area_and_perimeter_of_a_rectangle {

    
    public static void main(String[] args) {
        
        // The width and height must be 4.5 and 7.9
        double width = 4.5;
        double height = 7.9;
        double perimeter = (2 * width) + (2 * height);
        double area = width * height;
        System.out.println("The perimeter of the rectangle is equal to: " + perimeter);
        System.out.println("Area of the rectangle is equal to: " + area);
    }
    
}
